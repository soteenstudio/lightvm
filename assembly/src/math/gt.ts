export function gt_i32(a: i32, b: i32): i32 {
  return a > b;
}

export function gt_i64(a: i64, b: i64): i64 {
  return a > b;
}

export function gt_f32(a: f32, b: f32): f32 {
  return a > b;
}

export function gt_f64(a: f64, b: f64): f64 {
  return a > b;
}